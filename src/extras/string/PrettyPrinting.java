package extras.string;

public class PrettyPrinting {

    public static void main(String[] args) {

        float num = 12.67643344f;
        System.out.printf("The number is %.2f \n",num);

        System.out.printf("Pie is %.3f \n",Math.PI);

        System.out.printf("My name is %s and my age is %d","Biswajit",21);
    }
}
